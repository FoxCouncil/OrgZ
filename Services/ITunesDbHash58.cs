    /// <summary>
    /// Applies the hash58 checksum in place to a serialized iTunesDB.
    /// <paramref name="fireWireGuidHex"/> is the 16-hex-char FireWireGuid
    /// (e.g. "000A27001597690A"). Throws if the GUID is missing/invalid or the
    /// buffer is too small.
    /// </summary>
    public static void Apply(byte[] db, string? fireWireGuidHex)
    {
        if (db.Length < 0x6C)
        {
            throw new InvalidDataException("iTunesDB too small to hold a hash58 checksum.");
        }
        // The hash region (0x58..0x6C) must sit INSIDE the mhbd header - writing past headerSize
        // lands on the first child mhsd's magic and silently destroys the database.
        int mhbdHeaderSize = db[4] | (db[5] << 8) | (db[6] << 16) | (db[7] << 24);
        if (mhbdHeaderSize < 0x6C)
        {
            throw new InvalidDataException($"mhbd header (0x{mhbdHeaderSize:X}) is too small to hold the hash58 checksum region (needs ≥ 0x6C).");
        }
        var fwid = ParseFireWireGuid(fireWireGuidHex);

        // hashing_scheme @0x30 = HASH58 (1). Set before hashing - it's covered.
        db[0x30] = 1;
        db[0x31] = 0;

        // db_id (0x18, 8) and unk_0x32 (0x32, 20) are zeroed only for the hash
        // calculation, then restored; hash58 (0x58, 20) stays zero during it.
        var backup18 = db[0x18..0x20];
        var backup32 = db[0x32..0x46];
        Array.Clear(db, 0x18, 8);
        Array.Clear(db, 0x32, 20);
        Array.Clear(db, 0x58, 20);

        var key = SHA1.HashData(Concat(Fixed, DeriveY(fwid)));   // 20-byte HMAC key
        using var hmac = new HMACSHA1(key);                      // .NET zero-pads to 64
        var hash = hmac.ComputeHash(db);                         // 20 bytes

        Buffer.BlockCopy(hash, 0, db, 0x58, 20);
        Buffer.BlockCopy(backup18, 0, db, 0x18, 8);
        Buffer.BlockCopy(backup32, 0, db, 0x32, 20);
    }

    private static byte[] DeriveY(byte[] fwid)
    {
        var y = new byte[16];
        for (int i = 0; i < 4; i++)
        {
            int l = Lcm(fwid[i * 2], fwid[i * 2 + 1]);
            int hi = (l & 0xFF00) >> 8;
            int lo = l & 0xFF;
            y[i * 4]     = Table1[hi];
            y[i * 4 + 1] = Table2[hi];
            y[i * 4 + 2] = Table1[lo];
            y[i * 4 + 3] = Table2[lo];
        }
        return y;
    }

    private static int Gcd(int a, int b)
    {
        while (true)
        {
            a %= b;
            if (a == 0) return b;
            b %= a;
            if (b == 0) return a;
        }
    }

    private static int Lcm(int a, int b) => (a == 0 || b == 0) ? 1 : (a * b) / Gcd(a, b);

    private static byte[] ParseFireWireGuid(string? hex)
    {
        hex = hex?.Trim();
        if (string.IsNullOrEmpty(hex) || hex.Length < 16)
        {
            throw new InvalidOperationException("A 16-hex-char FireWireGuid is required for hash58 (Classic / Nano 3G+).");
        }
        var fwid = new byte[8];
        for (int i = 0; i < 8; i++)
        {
            fwid[i] = Convert.ToByte(hex.Substring(i * 2, 2), 16);
        }
        return fwid;
    }

    private static byte[] Concat(byte[] a, byte[] b)
    {
        var r = new byte[a.Length + b.Length];
        Buffer.BlockCopy(a, 0, r, 0, a.Length);
        Buffer.BlockCopy(b, 0, r, a.Length, b.Length);
        return r;
    }
